def add(num1, num2):
    num3 = 0
    num3 = float(num1) + float(num2)
    return num3


def subtract(num1, num2):
    num3 = 0
    num3 = float(num1) - float(num2)
    return num3


def multiply(num1, num2):
    num3 = 0
    num3 = float(num1) * float(num2)
    return num3


def divide(num1, num2):
    num3 = 0
    num3 = float(num1) / float(num2)
    return num3
